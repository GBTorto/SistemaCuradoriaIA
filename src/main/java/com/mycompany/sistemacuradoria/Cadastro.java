    private void cadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarButtonActionPerformed
        // TODO add your handling code here:
        String nome = cadastroTextFieldName.getText();
        String email = cadastroTextFieldEmail.getText();
        
        int idade = 0;
        try{
            idade = Integer.parseInt(cadastroTextFieldIdade.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "A idade deve ser um número válido", "Erro de entrada", JOptionPane.ERROR_MESSAGE);
        }
        
        String senha1 = new String(cadastroPasswordField1.getPassword());
        String senha2 = new String(cadastroPasswordField2.getPassword());
        
        if(senha1.equals(senha2)){
            User novoUsuario = new User();
            
            novoUsuario.setNome(nome);
            novoUsuario.setIdade(idade);
            novoUsuario.setTipo("Comum");
            novoUsuario.setSenha(senha1);
            novoUsuario.setEmail(email);
            
            int idNovoUsuario = novoUsuario.cadastrar();
            
            if(idNovoUsuario > 0){
                TelaInteresse telaInteresse = new TelaInteresse(idNovoUsuario);
                
                telaInteresse.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Erro ao salvar usuário. Tente novamente.", "Erro no Banco", JOptionPane.ERROR_MESSAGE);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "As senhas não conferem. Tente novamwente.");
        }
    }//GEN-LAST:event_cadastrarButtonActionPerformed

