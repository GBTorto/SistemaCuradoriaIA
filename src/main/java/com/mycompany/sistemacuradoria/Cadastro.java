    private void cadastroTextFieldIdadeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cadastroTextFieldIdadeKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        // !Character é uma classe utilitária do java; .isDigit verifica se o valor recebido é um número, se for diferente de número passa.
        if (!Character.isDigit(c) || cadastroTextFieldIdade.getText().length() >= 3){
            evt.consume(); // deleta tudo que não for número.
        }
    }//GEN-LAST:event_cadastroTextFieldIdadeKeyTyped

