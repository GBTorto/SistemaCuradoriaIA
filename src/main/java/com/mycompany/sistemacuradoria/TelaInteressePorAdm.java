    private void salvarPorAdmButtonInteresseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarPorAdmButtonInteresseActionPerformed
        // TODO add your handling code here:
        List<Integer> idsSelecionados = getInteressesSelecionados();
            
            if(idsSelecionados.isEmpty()){
                JOptionPane.showMessageDialog(this, "Selecione pelo menos um interesse", "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            try {
                CategoriaDAO dao = new CategoriaDAO();
                boolean sucesso = dao.salvarInteresse(this.idUsuario, idsSelecionados);

                if(sucesso){
                    JOptionPane.showMessageDialog(this, "Interesses salvos com sucesso!", "Concluído", JOptionPane.INFORMATION_MESSAGE);

                    // ************ ESTE CÓDIGO FINALMENTE SERÁ EXECUTADO ************
                    TabelaUsersAdmin telaAdmin = new TabelaUsersAdmin();
                    telaAdmin.setVisible(true);
                    telaAdmin.setLocationRelativeTo(null);
                    
                    this.dispose();

                } else {
                    // Este 'else' pode ser ativado se o DAO retornar false (e não lançar exceção)
                    JOptionPane.showMessageDialog(this, "Falha ao salvar interesses no banco de dados.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                // Bloco CATCH ESSENCIAL: Captura a falha do banco (SQLException, etc.)
                JOptionPane.showMessageDialog(this, "Erro de banco de dados ao salvar interesses: " + e.getMessage(), "Erro Crítico", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace(); // Imprime o erro no console
            }
    }//GEN-LAST:event_salvarPorAdmButtonInteresseActionPerformed

