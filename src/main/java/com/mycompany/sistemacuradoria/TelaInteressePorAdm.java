    private void inicializarInteresses(){
        CategoriaDAO dao = new CategoriaDAO();
        List<Categoria> categorias = dao.buscarTodas();
        
        painelInteresses.removeAll();
        
        for (Categoria cat : categorias){
            JCheckBox cb = new JCheckBox(cat.getNomeCategoria());
            
            System.out.println("DEBUG TELA: Nome recebido = '" + cat.getNomeCategoria() + "'");
            
            cb.setForeground(java.awt.Color.BLACK);
            cb.setOpaque(false);
            
            cb.setMaximumSize(new java.awt.Dimension(500, 30)); 
            
            // 2. FORÇAR ALINHAMENTO À ESQUERDA (Necessário para BoxLayout X_AXIS)
            cb.setAlignmentX(java.awt.Component.LEFT_ALIGNMENT);
            
            cb.setActionCommand(String.valueOf(cat.getIdCategoria()));
            
            cb.addItemListener(new ItemListener(){
                @Override
                public void itemStateChanged(ItemEvent e){
                    if(e.getStateChange() == ItemEvent.SELECTED){
                        if (contagemSelecionados >= 2){
                            ((JCheckBox) e.getItem()).setSelected(false);
                            JOptionPane.showMessageDialog(null, "Você só pode selecionar 2 interesses!", "Limite Atingido", JOptionPane.WARNING_MESSAGE);
                            return;
                        }
                        contagemSelecionados++;
                    }else if(e.getStateChange() == ItemEvent.DESELECTED){
                        contagemSelecionados--;
                    }
                }
            });
            
            listaCheckBoxes.add(cb);
            painelInteresses.add(cb);
        }
        
        painelInteresses.revalidate();
        painelInteresses.repaint();
    }
    
    private List<Integer> getInteressesSelecionados(){
        List <Integer> ids = new ArrayList<>();
        
        for (JCheckBox cb : listaCheckBoxes){
            if(cb.isSelected()){
                String idStr = cb.getActionCommand();
                ids.add(Integer.parseInt(idStr));
            }
        }
        
        return ids;
    }

