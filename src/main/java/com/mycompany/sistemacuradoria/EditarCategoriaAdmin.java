    private void salvarEditarCategoriaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarEditarCategoriaButtonActionPerformed
        // TODO add your handling code here:
        String sql = "update tb_categoria set categoria = ? where id_categoria = ?";
        
        String novoNomeCategoria = editarNomeCategoriaTextField.getText();
        
        if (novoNomeCategoria.trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "O nome da categoria n√£o pode ser vazio.", "Erro de Entrada", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        ConnectionFactory factory = new ConnectionFactory();
        
        try(Connection c = factory.obtemConexao();
            PreparedStatement ps = c.prepareStatement(sql)){
            
            ps.setString(1, novoNomeCategoria);
            ps.setInt(2, categoriaAtual.getIdCategoria());
            
            ps.executeUpdate();
            
            TabelaCategoriaAdmin telaTabelaCategoria = new TabelaCategoriaAdmin();
            telaTabelaCategoria.setVisible(true);
            telaTabelaCategoria.setLocationRelativeTo(null);
            this.dispose();
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_salvarEditarCategoriaButtonActionPerformed

